# endpoints
# ---------------------------------------------------------------------------

@router.get("", response_model=list[ControllerResponse])
async def list_controllers(
    project_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    result = await db.execute(
        select(Controller)
        .where(Controller.project_id == project_id)
        .order_by(Controller.created_at.desc())
    )
    return [ControllerResponse.model_validate(c) for c in result.scalars().all()]


@router.post("", response_model=ControllerResponse, status_code=status.HTTP_201_CREATED)
async def create_controller(
    project_id: UUID,
    body: ControllerCreate,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    ctrl = Controller(project_id=project_id, **body.model_dump(exclude_unset=True))
    db.add(ctrl)
    await db.flush()
    await db.refresh(ctrl)
    return ControllerResponse.model_validate(ctrl)


@router.get("/{controller_id}", response_model=ControllerResponse)
async def get_controller(
    project_id: UUID,
    controller_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    ctrl = await _get_controller_or_404(controller_id, project_id, db)
    return ControllerResponse.model_validate(ctrl)


@router.put("/{controller_id}", response_model=ControllerResponse)
async def update_controller(
    project_id: UUID,
    controller_id: UUID,
    body: ControllerUpdate,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    ctrl = await _get_controller_or_404(controller_id, project_id, db)

    for field, value in body.model_dump(exclude_unset=True).items():
        setattr(ctrl, field, value)

    ctrl.version += 1
    await db.flush()
    await db.refresh(ctrl)
    return ControllerResponse.model_validate(ctrl)


@router.delete("/{controller_id}", status_code=status.HTTP_204_NO_CONTENT)
async def delete_controller(
    project_id: UUID,
    controller_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    ctrl = await _get_controller_or_404(controller_id, project_id, db)
    await db.delete(ctrl)
    await db.flush()


@router.get("/{controller_id}/preview", response_class=PlainTextResponse)
async def preview_controller(
    project_id: UUID,
    controller_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Preview the ServoDyn input file for this controller."""
    project = await _verify_project(project_id, current_user.org_id, db)
    ctrl = await _get_controller_or_404(controller_id, project_id, db)
    return _generate_servodyn_file(ctrl, project)
