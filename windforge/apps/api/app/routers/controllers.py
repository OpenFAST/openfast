"""Controller CRUD endpoints with ServoDyn file preview."""

from uuid import UUID

from fastapi import APIRouter, Depends, HTTPException, status
from fastapi.responses import PlainTextResponse
from sqlalchemy import select
from sqlalchemy.ext.asyncio import AsyncSession

from app.database import get_db
from app.dependencies import get_current_user
from app.models.components import Controller
from app.models.project import Project
from app.models.user import User
from app.schemas.components import ControllerCreate, ControllerResponse, ControllerUpdate

router = APIRouter(prefix="/projects/{project_id}/controllers", tags=["controllers"])


# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------
async def _verify_project(project_id: UUID, org_id: UUID, db: AsyncSession) -> Project:
    result = await db.execute(
        select(Project).where(Project.id == project_id, Project.org_id == org_id)
    )
    project = result.scalar_one_or_none()
    if project is None:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Project not found")
    return project


async def _get_controller_or_404(
    controller_id: UUID, project_id: UUID, db: AsyncSession
) -> Controller:
    result = await db.execute(
        select(Controller).where(
            Controller.id == controller_id, Controller.project_id == project_id
        )
    )
    ctrl = result.scalar_one_or_none()
    if ctrl is None:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND, detail="Controller not found"
        )
    return ctrl


def _generate_servodyn_file(ctrl: Controller, project: Project) -> str:
    """Generate a ServoDyn input file from controller parameters."""
    params = ctrl.parameters or {}
    lines: list[str] = []
    lines.append("------- SERVODYN INPUT FILE ------------------------------------------------")
    lines.append(f"Generated by WindForge for controller: {ctrl.name} v{ctrl.version}")
    lines.append(f"Project: {project.name}")
    lines.append(
        "---------------------- SIMULATION CONTROL ----------------------------------"
    )
    lines.append(f"{0:<14}   Echo       - Echo input data to <RootName>.ech (flag)")
    lines.append(
        f"{project.dt or 0.005:<14}   DT         - Communication interval (s)"
    )
    lines.append(
        "---------------------- PITCH CONTROL ---------------------------------------"
    )
    lines.append(f"{ctrl.pcmode:<14}   PCMode     - Pitch control mode")
    lines.append(
        f"{params.get('tpitmanS', 9999.9):<14}   TPitManS(1)  - Time to start pitch maneuver (s)"
    )
    lines.append(
        f"{params.get('pitManRat', 2.0):<14}   PitManRat(1) - Pitch rate at which maneuver executes (deg/s)"
    )
    lines.append(
        f"{params.get('blPitchF', 0.0):<14}   BlPitchF(1)  - Blade pitch at end of maneuver (deg)"
    )
    lines.append(
        "---------------------- GENERATOR AND TORQUE CONTROL -------------------------"
    )
    lines.append(f"{ctrl.vscontrl:<14}   VSContrl   - Variable-speed control mode")
    lines.append(
        f"{params.get('genEff', 94.4):<14}   GenEff     - Generator efficiency (%)"
    )
    lines.append(
        f"{params.get('genTiStr', True)!s:<14}   GenTiStr   - Generator timing starter (flag)"
    )
    lines.append(
        f"{params.get('genTiStp', True)!s:<14}   GenTiStp   - Generator timing stopper (flag)"
    )
    lines.append(
        f"{params.get('spdGenOn', 9999.9):<14}   SpdGenOn   - Generator speed to turn on (rpm)"
    )
    lines.append(
        f"{params.get('timGenOn', 0.0):<14}   TimGenOn   - Generator start time (s)"
    )
    lines.append(
        f"{params.get('timGenOf', 9999.9):<14}   TimGenOf   - Generator stop time (s)"
    )

    # DLL / external controller
    lines.append(
        "---------------------- BLADED-STYLE DISCRETE-CONTROLLER INTERFACE -----------"
    )
    dll_fn = ctrl.dll_filename or "unused"
    dll_proc = ctrl.dll_procname or "DISCON"
    lines.append(f'"{dll_fn}"    DLL_FileName  - DLL file name')
    lines.append(f'"{dll_proc}"  DLL_ProcName  - DLL procedure name')

    # Additional custom parameters
    if params:
        lines.append(
            "---------------------- CONTROLLER PARAMETERS --------------------------------"
        )
        skip_keys = {
            "tpitmanS", "pitManRat", "blPitchF", "genEff",
            "genTiStr", "genTiStp", "spdGenOn", "timGenOn", "timGenOf",
        }
        for key, val in params.items():
            if key not in skip_keys:
                lines.append(f"{val!s:<14}   {key}")

    lines.append(
        "---------------------- END OF FILE -----------------------------------------"
    )
    return "\n".join(lines)


