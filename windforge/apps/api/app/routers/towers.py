# ---- GET / ----------------------------------------------------------------
@router.get("/", response_model=list[TowerResponse])
async def list_towers(
    project_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    result = await db.execute(
        select(Tower)
        .where(Tower.project_id == project_id)
        .order_by(Tower.created_at.desc())
    )
    return [TowerResponse.model_validate(t) for t in result.scalars().all()]


# ---- POST / ---------------------------------------------------------------
@router.post("/", response_model=TowerResponse, status_code=status.HTTP_201_CREATED)
async def create_tower(
    project_id: UUID,
    body: TowerCreate,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)

    data = body.model_dump(exclude_unset=True)
    # Serialize station sub-models to dicts for JSON column
    if "stations" in data and data["stations"] is not None:
        data["stations"] = [
            s.model_dump() if hasattr(s, "model_dump") else s for s in data["stations"]
        ]

    tower = Tower(project_id=project_id, **data)
    db.add(tower)
    await db.flush()
    await db.refresh(tower)
    return TowerResponse.model_validate(tower)


# ---- GET /{id} -------------------------------------------------------------
@router.get("/{tower_id}", response_model=TowerResponse)
async def get_tower(
    project_id: UUID,
    tower_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    tower = await _get_tower_or_404(tower_id, project_id, db)
    return TowerResponse.model_validate(tower)


# ---- PUT /{id} -------------------------------------------------------------
@router.put("/{tower_id}", response_model=TowerResponse)
async def update_tower(
    project_id: UUID,
    tower_id: UUID,
    body: TowerUpdate,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    tower = await _get_tower_or_404(tower_id, project_id, db)

    update_data = body.model_dump(exclude_unset=True)
    if "stations" in update_data and update_data["stations"] is not None:
        update_data["stations"] = [
            s.model_dump() if hasattr(s, "model_dump") else s for s in update_data["stations"]
        ]

    for field, value in update_data.items():
        setattr(tower, field, value)

    tower.version += 1
    await db.flush()
    await db.refresh(tower)
    return TowerResponse.model_validate(tower)


# ---- DELETE /{id} ----------------------------------------------------------
@router.delete("/{tower_id}", status_code=status.HTTP_204_NO_CONTENT)
async def delete_tower(
    project_id: UUID,
    tower_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    await _verify_project(project_id, current_user.org_id, db)
    tower = await _get_tower_or_404(tower_id, project_id, db)
    await db.delete(tower)
    await db.flush()


# ---- GET /{id}/preview — ElastoDyn tower file preview ----------------------
@router.get("/{tower_id}/preview", response_class=PlainTextResponse)
async def preview_tower(
    project_id: UUID,
    tower_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Return the generated ElastoDyn tower input file content."""
    project = await _verify_project(project_id, current_user.org_id, db)
    tower = await _get_tower_or_404(tower_id, project_id, db)
    return _generate_elastodyn_tower_file(tower, project)
