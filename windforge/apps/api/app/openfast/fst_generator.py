"""
OpenFAST primary (.fst) input file generator.

Generates the main .fst file matching the EXACT parse order from
FAST_Subs.f90 subroutine FAST_ReadPrimaryFile (lines 2377-3200+).

Parse order verified against OpenFAST v3.5.x source:
  1. Header (2 lines)
  2. SIMULATION CONTROL: Echo, AbortLevel, TMax, DT, InterpOrder,
     NumCrctn, DT_UJac, UJacSclFact
  3. FEATURE SWITCHES: CompElast, CompInflow, CompAero, CompServo,
     CompHydro, CompSub, CompMooring, CompIce, MHK
  4. ENVIRONMENTAL CONDITIONS: Gravity, AirDens, WtrDens, KinVisc,
     SpdSound, Patm, Pvap, WtrDpth, MSL2SWL
  5. INPUT FILES: EDFile, BDBldFile(1..3), InflowFile, AeroFile,
     ServoFile, HydroFile, SubFile, MooringFile, IceFile
  6. OUTPUT: SumPrint, SttsTime, ChkptTime, DT_Out, TStart,
     OutFileFmt, TabDelim, OutFmt
  7. LINEARIZATION: Linearize, CalcSteady, TrimCase, TrimTol,
     TrimGain, Twr_Kdmp, Bld_Kdmp, NLinTimes, LinTimes,
     LinInputs, LinOutputs, LinOutJac, LinOutMod
  8. VISUALIZATION: WrVTK, VTK_type, VTK_fields, VTK_fps
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Optional


@dataclass
class FSTConfig:
    """Configuration parameters for the OpenFAST primary (.fst) file."""

    # --- Simulation Control ---
    echo: bool = False
    abort_level: str = "FATAL"
    t_max: float = 630.0
    dt: float = 0.005
    interp_order: int = 1
    num_crctn: int = 0
    dt_ujac: float = 99999.0
    ujac_scl_fact: float = 1.0E+06

    # --- Feature Switches ---
    comp_elast: int = 1       # 1=ElastoDyn, 2=ElastoDyn+BeamDyn
    comp_inflow: int = 1      # 0=still air, 1=InflowWind
    comp_aero: int = 2        # 0=None, 2=AeroDyn
    comp_servo: int = 1       # 0=None, 1=ServoDyn
    comp_hydro: int = 0       # 0=None, 1=HydroDyn
    comp_sub: int = 0         # 0=None, 1=SubDyn
    comp_mooring: int = 0     # 0=None, 1=MAP, 2=FEAM, 3=MoorDyn, 4=OrcaFlex
    comp_ice: int = 0         # 0=None, 1=IceFloe, 2=IceDyn
    mhk: int = 0              # 0=Not MHK, 1=Fixed MHK, 2=Floating MHK

    # --- Environmental Conditions ---
    gravity: float = 9.80665
    air_dens: float = 1.225
    wtr_dens: float = 1025.0
    kin_visc: float = 1.464E-05
    spd_sound: float = 335.0
    patm: float = 103500.0
    pvap: float = 1700.0
    wtr_dpth: float = 0.0
    msl2swl: float = 0.0

    # --- Input Files ---
    ed_file: str = "NRELOffs662hrBl_ElastoDyn.dat"
    bd_bld_file_1: str = "unused"
    bd_bld_file_2: str = "unused"
    bd_bld_file_3: str = "unused"
    inflow_file: str = "NRELOffs662hrBl_InflowWind.dat"
    aero_file: str = "NRELOffs662hrBl_AeroDyn.dat"
    servo_file: str = "NRELOffs662hrBl_ServoDyn.dat"
    hydro_file: str = "unused"
    sub_file: str = "unused"
    mooring_file: str = "unused"
    ice_file: str = "unused"

    # --- Output ---
    sum_print: bool = False
    stts_time: float = 10.0
    chkpt_time: float = 99999.0
    dt_out: str = "default"
    t_start: float = 0.0
    out_file_fmt: int = 2     # 1=text, 2=binary, 3=both
    tab_delim: bool = True
    out_fmt: str = "ES10.3E2"

    # --- Linearization ---
    linearize: bool = False
    calc_steady: bool = False
    trim_case: int = 3
    trim_tol: float = 0.001
    trim_gain: float = 0.01
    twr_kdmp: float = 0.0
    bld_kdmp: float = 0.0
    n_lin_times: int = 2
    lin_times: list[float] = field(default_factory=lambda: [30.0, 60.0])
    lin_inputs: int = 1
    lin_outputs: int = 1
    lin_out_jac: bool = False
    lin_out_mod: bool = False

    # --- Visualization ---
    wr_vtk: int = 0           # 0=none, 1=init, 2=animation, 3=both
    vtk_type: int = 1         # 1=surfaces, 2=basic lines, 3=all meshes
    vtk_fields: bool = False
    vtk_fps: float = 30.0


class FSTGenerator:
    """Generates the OpenFAST primary .fst input file.

    The field order strictly follows the parse order in FAST_Subs.f90
    subroutine ``FAST_ReadPrimaryFile``.
    """

    def generate(self, config: FSTConfig, root_name: str = "WindForge") -> str:
        """Generate the complete .fst file content.

        Parameters
        ----------
        config : FSTConfig
            All parameters needed for the .fst file.
        root_name : str
            Root name used in the file header description.

        Returns
        -------
        str
            Complete .fst file content ready to write to disk.
        """
        lines: list[str] = []
        _a = lines.append

        # ----------------------------------------------------------------
        # HEADER (2 lines read by ReadCom + ReadStr)
        # ----------------------------------------------------------------
        _a(f"------- OpenFAST INPUT FILE -------------------------------------------")
        _a(f"{root_name} simulation generated by WindForge")

        # ----------------------------------------------------------------
        # SIMULATION CONTROL
        # ----------------------------------------------------------------
        _a("---------------------- SIMULATION CONTROL --------------------------------------")
        _a(f"{_flag(config.echo):<14s}   Echo            - Echo input data to <RootName>.ech (flag)")
        _a(f'{"\"" + config.abort_level + "\"":<14s}   AbortLevel      - Error level when simulation should abort (string) {{\"WARNING\", \"SEVERE\", \"FATAL\"}}')
        _a(f"{config.t_max:<14.4f}   TMax            - Total run time (s)")
        _a(f"{config.dt:<14.6f}   DT              - Recommended module time step (s)")
        _a(f"{config.interp_order:<14d}   InterpOrder     - Interpolation order for input/output time history {{1=linear, 2=quadratic}} (-)")
        _a(f"{config.num_crctn:<14d}   NumCrctn        - Number of correction iterations {{0=explicit calculation}} (-)")
        _a(f"{config.dt_ujac:<14.1f}   DT_UJac         - Time between calls to get Jacobians (s)")
        _a(f"{config.ujac_scl_fact:<14.1E}   UJacSclFact     - Scaling factor used in Jacobians (-)")

        # ----------------------------------------------------------------
        # FEATURE SWITCHES AND FLAGS
        # ----------------------------------------------------------------
        _a("---------------------- FEATURE SWITCHES AND FLAGS ------------------------------")
        _a(f"{config.comp_elast:<14d}   CompElast       - Compute structural dynamics (switch) {{1=ElastoDyn; 2=ElastoDyn + BeamDyn for blades}}")
        _a(f"{config.comp_inflow:<14d}   CompInflow      - Compute inflow wind velocities (switch) {{0=still air; 1=InflowWind; 2=external from OpenFOAM}}")
        _a(f"{config.comp_aero:<14d}   CompAero        - Compute aerodynamic loads (switch) {{0=None; 1=AeroDyn14; 2=AeroDyn15}}")
        _a(f"{config.comp_servo:<14d}   CompServo       - Compute control and electrical-drive dynamics (switch) {{0=None; 1=ServoDyn}}")
        _a(f"{config.comp_hydro:<14d}   CompHydro       - Compute hydrodynamic loads (switch) {{0=None; 1=HydroDyn}}")
        _a(f"{config.comp_sub:<14d}   CompSub         - Compute sub-structural dynamics (switch) {{0=None; 1=SubDyn; 2=External Platform MCKF}}")
        _a(f"{config.comp_mooring:<14d}   CompMooring     - Compute mooring system (switch) {{0=None; 1=MAP++; 2=FEAMooring; 3=MoorDyn; 4=OrcaFlex}}")
        _a(f"{config.comp_ice:<14d}   CompIce         - Compute ice loads (switch) {{0=None; 1=IceFloe; 2=IceDyn}}")
        _a(f"{config.mhk:<14d}   MHK             - MHK turbine type (switch) {{0=Not an MHK turbine; 1=Fixed MHK turbine; 2=Floating MHK turbine}}")

        # ----------------------------------------------------------------
        # ENVIRONMENTAL CONDITIONS
        # ----------------------------------------------------------------
        _a("---------------------- ENVIRONMENTAL CONDITIONS --------------------------------")
        _a(f"{config.gravity:<14.5f}   Gravity         - Gravitational acceleration (m/s^2)")
        _a(f"{config.air_dens:<14.4f}   AirDens         - Air density (kg/m^3)")
        _a(f"{config.wtr_dens:<14.1f}   WtrDens         - Water density (kg/m^3)")
        _a(f"{config.kin_visc:<14.4E}   KinVisc         - Kinematic viscosity of working fluid (m^2/s)")
        _a(f"{config.spd_sound:<14.1f}   SpdSound        - Speed of sound in working fluid (m/s)")
        _a(f"{config.patm:<14.1f}   Patm            - Atmospheric pressure (Pa) [used only when CavitCheck=True]")
        _a(f"{config.pvap:<14.1f}   Pvap            - Vapour pressure of working fluid (Pa) [used only when CavitCheck=True]")
        _a(f"{config.wtr_dpth:<14.1f}   WtrDpth         - Water depth (m)")
        _a(f"{config.msl2swl:<14.1f}   MSL2SWL         - Offset between still-water level and mean sea level (m) [positive upward]")

        # ----------------------------------------------------------------
        # INPUT FILES
        # ----------------------------------------------------------------
        _a("---------------------- INPUT FILES ---------------------------------------------")
        _a(f'{"\"" + config.ed_file + "\"":<40s}   EDFile          - Name of file containing ElastoDyn input parameters (-)')
        _a(f'{"\"" + config.bd_bld_file_1 + "\"":<40s}   BDBldFile(1)    - Name of file containing BeamDyn input parameters for blade 1 (-)')
        _a(f'{"\"" + config.bd_bld_file_2 + "\"":<40s}   BDBldFile(2)    - Name of file containing BeamDyn input parameters for blade 2 (-)')
        _a(f'{"\"" + config.bd_bld_file_3 + "\"":<40s}   BDBldFile(3)    - Name of file containing BeamDyn input parameters for blade 3 (-)')
        _a(f'{"\"" + config.inflow_file + "\"":<40s}   InflowFile      - Name of file containing inflow wind input parameters (-)')
        _a(f'{"\"" + config.aero_file + "\"":<40s}   AeroFile        - Name of file containing aerodynamic input parameters (-)')
        _a(f'{"\"" + config.servo_file + "\"":<40s}   ServoFile       - Name of file containing control and electrical-drive input parameters (-)')
        _a(f'{"\"" + config.hydro_file + "\"":<40s}   HydroFile       - Name of file containing hydrodynamic input parameters (-)')
        _a(f'{"\"" + config.sub_file + "\"":<40s}   SubFile         - Name of file containing sub-structural input parameters (-)')
        _a(f'{"\"" + config.mooring_file + "\"":<40s}   MooringFile     - Name of file containing mooring system input parameters (-)')
        _a(f'{"\"" + config.ice_file + "\"":<40s}   IceFile         - Name of file containing ice input parameters (-)')

        # ----------------------------------------------------------------
        # OUTPUT
        # ----------------------------------------------------------------
        _a("---------------------- OUTPUT --------------------------------------------------")
        _a(f"{_flag(config.sum_print):<14s}   SumPrint        - Print summary data to <RootName>.sum (flag)")
        _a(f"{config.stts_time:<14.1f}   SttsTime        - Amount of time between screen status messages (s)")
        _a(f"{config.chkpt_time:<14.1f}   ChkptTime       - Amount of time between creating checkpoint files for potential restart (s)")
        _a(f'{"\"" + config.dt_out + "\"":<14s}   DT_Out          - Time step for tabular output (s) (or \"default\")')
        _a(f"{config.t_start:<14.1f}   TStart          - Time to begin tabular output (s)")
        _a(f"{config.out_file_fmt:<14d}   OutFileFmt      - Format for tabular (time-marching) output file (switch) {{1: text file [<RootName>.out], 2: binary file [<RootName>.outb], 3: both}}")
        _a(f"{_flag(config.tab_delim):<14s}   TabDelim        - Use tab delimiters in text tabular output file? (flag) (currently unused)")
        _a(f'{"\"" + config.out_fmt + "\"":<14s}   OutFmt          - Format used for text tabular output, excluding the time channel. Resulting field should be 10 characters. (quoted string)')

        # ----------------------------------------------------------------
        # LINEARIZATION
        # ----------------------------------------------------------------
        _a("---------------------- LINEARIZATION -------------------------------------------")
        _a(f"{_flag(config.linearize):<14s}   Linearize       - Linearization analysis (flag)")
        _a(f"{_flag(config.calc_steady):<14s}   CalcSteady      - Calculate a steady-state periodic operating point before linearization? [unused if Linearize=False] (flag)")
        _a(f"{config.trim_case:<14d}   TrimCase        - Controller parameter to be trimmed {{1:yaw; 2:torque; 3:pitch}} [used only if CalcSteady=True] (-)")
        _a(f"{config.trim_tol:<14.4E}   TrimTol         - Tolerance for the rotational speed convergence [used only if CalcSteady=True] (-)")
        _a(f"{config.trim_gain:<14.4E}   TrimGain        - Proportional gain for the rotational speed error (>0) [used only if CalcSteady=True] (rad/(rad/s) for yaw or pitch; Nm/(rad/s) for torque)")
        _a(f"{config.twr_kdmp:<14.1f}   Twr_Kdmp        - Damping factor for the tower [used only if CalcSteady=True] (N/(m/s))")
        _a(f"{config.bld_kdmp:<14.1f}   Bld_Kdmp        - Damping factor for the blades [used only if CalcSteady=True] (N/(m/s))")
        _a(f"{config.n_lin_times:<14d}   NLinTimes       - Number of times to linearize (-) [>=1] [unused if Linearize=False]")
        lin_times_str = ", ".join(f"{t:.1f}" for t in config.lin_times)
        _a(f"{lin_times_str:<40s}   LinTimes        - List of times at which to linearize (s) [1 to NLinTimes] [used only when Linearize=True and CalcSteady=False]")
        _a(f"{config.lin_inputs:<14d}   LinInputs       - Inputs included in linearization (switch) {{0=none; 1=standard; 2=all module inputs (debug)}} [unused if Linearize=False]")
        _a(f"{config.lin_outputs:<14d}   LinOutputs      - Outputs included in linearization (switch) {{0=none; 1=from OutList(s); 2=all module outputs (debug)}} [unused if Linearize=False]")
        _a(f"{_flag(config.lin_out_jac):<14s}   LinOutJac       - Include full Jacobians in linearization output (for debug) (flag) [unused if Linearize=False]")
        _a(f"{_flag(config.lin_out_mod):<14s}   LinOutMod       - Write module-level linearization output files in addition to output for full system? (flag) [unused if Linearize=False]")

        # ----------------------------------------------------------------
        # VISUALIZATION
        # ----------------------------------------------------------------
        _a("---------------------- VISUALIZATION ------------------------------------------")
        _a(f"{config.wr_vtk:<14d}   WrVTK           - VTK visualization data output: (switch) {{0=none; 1=initialization data only; 2=animation; 3=mode shapes}}")
        _a(f"{config.vtk_type:<14d}   VTK_type        - Type of VTK visualization data: (switch) {{1=surfaces; 2=basic meshes (lines/points); 3=all meshes (debug)}}")
        _a(f"{_flag(config.vtk_fields):<14s}   VTK_fields      - Write mesh fields to VTK data files? (flag) {{true/false}}")
        _a(f"{config.vtk_fps:<14.1f}   VTK_fps         - Frame rate for VTK output (frames per second) {{will use closest integer multiple of DT}} [used only if WrVTK=2 or WrVTK=3]")

        return "\n".join(lines) + "\n"


def _flag(value: bool) -> str:
    """Convert a boolean to OpenFAST flag format."""
    return "True" if value else "False"
