    !  INTEGRATE BY GAUSSIAN QUADRATURE

    DO N = 1,p%NGAUSS
        W = p%GAUSSW(N)

        DO I = 1,4
            p%AM(I)=p%AM(I)+p%SHAP(N,I)*W

            DO L = 1,4
