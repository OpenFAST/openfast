                DO M = 1,4
                    DO K = 1,4
                        p%RADDM(I,L,M,K) = p%RADDM(I,L,M,K) + p%SHAPS(N,I)*p%SHAPS(N,L)*p%SHAP(N,M)*p%SHAP(N,K)*W
                    ENDDO
                ENDDO
            ENDDO
        ENDDO

        DO I = 1,3
            p%PM(I) = p%PM(I) + PS(N,I)*W
            DO L = 1,3
                p%PMPN(I,L) = p%PMPN(I,L) + PS(N,I)*PS(N,L)*W
