       NULLIFY( OutputData%Fz )
    ELSE
       CALL C_F_POINTER(OutputData%C_obj%Fz, OutputData%Fz, (/OutputData%C_obj%Fz_Len/))
    END IF

    ! -- wrtOutput Output Data fields
    IF ( .NOT. C_ASSOCIATED( OutputData%C_obj%wrtOutput ) ) THEN
