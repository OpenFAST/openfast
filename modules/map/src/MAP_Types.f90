   else if (associated(DstInputData%z)) then
      deallocate(DstInputData%z)
   end if
   call MeshCopy(SrcInputData%PtFairDisplacement, DstInputData%PtFairDisplacement, CtrlCode, ErrStat2, ErrMsg2 )
