    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%vt_tot,2)
    Int_Xferred = Int_Xferred + 2

      DO i2 = LBOUND(InData%vt_tot,2), UBOUND(InData%vt_tot,2)
