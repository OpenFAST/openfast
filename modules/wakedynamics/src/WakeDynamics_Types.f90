    IntKiBuf( Int_Xferred    ) = LBOUND(InData%vt_amb,2)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%vt_amb,2)
    Int_Xferred = Int_Xferred + 2

      DO i2 = LBOUND(InData%vt_amb,2), UBOUND(InData%vt_amb,2)
