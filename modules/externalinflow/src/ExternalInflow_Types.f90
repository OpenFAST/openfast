      deallocate(OutputData%u)
      OutputData%u => null()
      OutputData%C_obj%u = c_null_ptr
      OutputData%C_obj%u_Len = 0
   end if
   if (associated(OutputData%v)) then
      deallocate(OutputData%v)
      OutputData%v => null()
