         call SetErrStat(ErrStat2, ErrMsg2, ErrStat, ErrMsg, RoutineName)
         if (ErrStat >= AbortErrLev) return
      end do
   end if
   if (allocated(SrcOutputData%WAMIT2)) then
      LB(1:1) = lbound(SrcOutputData%WAMIT2, kind=B8Ki)
