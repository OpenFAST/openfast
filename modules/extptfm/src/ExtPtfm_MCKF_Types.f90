    Int_BufSz   = Int_BufSz   + 2*1  ! qm upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%qm)  ! qm
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! qmdot allocated yes/no
  IF ( ALLOCATED(InData%qmdot) ) THEN
