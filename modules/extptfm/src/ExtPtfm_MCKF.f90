   call LAPACK_GEMM( 'T', 'T', 1.0_ReKi, Vt, U, 0.0_ReKi, Ainv, ErrStat, ErrMsg)
   ! --- Compute rank
   !tol=maxval(shape(A))*epsilon(maxval(S))
   !rank=0
   !do i=1,K
   !   if(S(i) .gt. tol)then
   !      rank=rank+1
   !   end if
   !end do
   !print*,'Rank',rank
   !   Ainv=transpose(matmul(matmul(U(:,1:r),S_inv(1:r,1:r)),Vt(1:r,:)))
   END SUBROUTINE PseudoInverse

