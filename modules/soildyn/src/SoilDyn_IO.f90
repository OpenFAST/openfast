   integer(IntKi)                                     :: IOS                  !< Temporary error status

      ! Initialize ErrStat
   ErrStat = ErrID_None
   ErrMsg  = ""

    select case(InputFileData%CalcOption)
      case (Calc_StiffDamp)
         call ValidateStiffnessMatrix()
      case (Calc_PYcurve)
         call ValidatePYcurves()
      case (Calc_REDWIN)
         call ValidateDLL()
   end select


