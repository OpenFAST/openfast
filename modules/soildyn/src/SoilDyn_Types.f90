        Int_Xferred = Int_Xferred + Buf_size
      END IF
      CALL SlD_Unpackredwindllstates( Re_Buf, Db_Buf, Int_Buf, OutData%dll_states(i1), ErrStat2, ErrMsg2 ) ! dll_states 
        CALL SetErrStat(ErrStat2, ErrMsg2, ErrStat, ErrMsg, RoutineName)
