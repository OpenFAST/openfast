!
!  f(t) = a + b * t + c * t**2
!
!  where a, b and c are determined as the solution to
!  f(t1) = u1, f(t2) = u2, f(t3) = u3
!
!..................................................................................................................................

   TYPE(DWM_InputType), INTENT(IN)  :: u1      ! Input at t1 > t2 > t3
   TYPE(DWM_InputType), INTENT(IN)  :: u2      ! Input at t2 > t3
