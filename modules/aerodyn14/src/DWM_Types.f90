   integer(IntKi)  :: LB(2), UB(2)
   integer(IntKi)  :: stat
   logical         :: IsAllocAssoc
   if (Buf%ErrStat /= ErrID_None) return
   call InflowWind_UnpackMisc(Buf, OutData%IfW) ! IfW 
   call RegUnpack(Buf, OutData%position_y)
