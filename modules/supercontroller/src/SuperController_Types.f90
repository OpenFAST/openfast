    Int_Xferred = Int_Xferred + 1
    IntKiBuf(Int_Xferred) = InData%NumStatesTurbine
    Int_Xferred = Int_Xferred + 1
    IntKiBuf(Int_Xferred) = InData%NumParamGlobal
    Int_Xferred = Int_Xferred + 1
    IntKiBuf(Int_Xferred) = InData%NumParamTurbine
    Int_Xferred = Int_Xferred + 1
