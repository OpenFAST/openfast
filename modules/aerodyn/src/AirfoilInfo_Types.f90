      OutData%ColCpmin = IntKiBuf( Int_Xferred ) 
      Int_Xferred   = Int_Xferred + 1
  IF ( IntKiBuf( Int_Xferred ) == 0 ) THEN  ! AFInfo not allocated
    Int_Xferred = Int_Xferred + 1
  ELSE
