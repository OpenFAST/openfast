  ErrMsg  = ""
  Re_Xferred  = 1
  Db_Xferred  = 1
  Int_Xferred  = 1
      OutData%ColCd = IntKiBuf( Int_Xferred ) 
      Int_Xferred   = Int_Xferred + 1
