    Int_BufSz   = Int_BufSz   + 2*1  ! CpminAoAknots upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%CpminAoAknots)  ! CpminAoAknots
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! CpminReKnots allocated yes/no
