    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%Coefs,2)
    Int_Xferred = Int_Xferred + 2

      IF (SIZE(InData%Coefs)>0) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%Coefs))-1 ) = PACK(InData%Coefs,.TRUE.)
