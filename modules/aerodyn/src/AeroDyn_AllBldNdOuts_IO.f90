                     OutIdx = OutIdx + 1
                  ENDDO
               ENDDO
            endif

 

            !================================================
            ! Inductions in polar rotating hub coordinates
            ! Axial induction, polar rotating hub coordinates
         CASE ( BldNd_Uin )
