                     OutIdx = OutIdx + 1
                  ENDDO
               ENDDO
            endif
 
            ! Radial induction, polar rotating hub coordinates
         CASE ( BldNd_Uir )
            if (p_AD%WakeMod /= WakeMod_FVW) then
