               DO IdxBlade=1,p%BldNd_BladesOut
                  DO IdxNode=1,u%BladeMotion(IdxBlade)%NNodes
!NOT available in BEMT/DBEMT yet
                     y%WriteOutput( OutIdx ) = 0.0_ReKi
