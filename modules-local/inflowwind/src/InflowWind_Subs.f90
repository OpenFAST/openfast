   IF (ErrStat >= AbortErrLev) THEN
      CALL Cleanup()
      RETURN
   END IF


      ! Tell the users what we are doing... though this part should be rather quick.
   CALL WrScr( ' Opening InflowWind input file:  '//InputFileName )


