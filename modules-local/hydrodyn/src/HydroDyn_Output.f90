      WRITE(p%UnOutFile,Frmt,ADVANCE='no')   ( p%Delim,  y%Morison%WriteOutput(I), I=1,p%Morison%NumOuts )
   END IF
      
      
   WRITE (p%UnOutFile,'()', IOSTAT=ErrStat)          ! write the line return
   
   RETURN


END SUBROUTINE HDOut_WriteOutputs



