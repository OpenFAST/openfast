      call cleanup()
      return
   end if
   CALL Conv2UC( Line )
   IF ( INDEX(Line, "DEFAULT" ) .EQ. 1) THEN
       InputFileData%tngt_stf_pert = 1.0D-06
   ELSE ! If it's not "default", read this variable;
